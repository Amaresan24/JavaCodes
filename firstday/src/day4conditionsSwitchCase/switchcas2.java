package day4conditionsSwitchCase;

public class switchcas2 {
public static void main (String args[]) {
/// Movie  Reatings using Switch case.
	
int reating = 1;

switch (reating) {
case 1:
	System.out.println("poor movie");
break;
case 2:
	System.out.println("below Average movie");
break;
case 3:
	System.out.println("Above Average movie");
break;
case 4:
	System.out.println("Hit  movie");
	break;
case 5:
	System.out.println("Block Buster Movie");

default:
	System.out.println("Invalid Reating");


}
	
}	
}
