package day4conditionsSwitchCase;

public class switchCase1 {
	
public static void main (String args[]) {
String animal = "lion";

switch(animal) {

case "lion":
	System.out.println("King of Forest");
	break;
case "Flower":
	System.out.println("Rose");
	break;

default :
System.out.println("Invaild");
break;
}
	
}
	
}


